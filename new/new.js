/* Quando queremos criar um novo objeto com algumas predefinições já prontas. podemos usar o método new().

 Assim como na função construtora. O método retornará um objeto com as informações que enviamos pelo parâmetro. Exemplo*/

const text = new String('love as you aways loved')
console.log(text)

/*O resultado será um objeto com o texto inserido dentro dos parâmetros, com cada letra sendo uma posição de um array dentro desse objeto. */